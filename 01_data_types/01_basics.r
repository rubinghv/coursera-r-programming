# sequence
x <- 1:20
print(length(x)) # get length

# empty vector
x <- vector()
print(x)

# all numbers are numeric by default
# to get integer append L
x_integer <- 1L
print(x_integer)

# INF is special value
infinity <- 1/0
print(infinity)

# error value is NaN
not_a_number <- 0/0
print(not_a_number)